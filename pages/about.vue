        <h1>About</h1>

        <p>While most cancers are due to acquired changes in genes (mutations), around 5-10% of cancers are due to an inherited mutation. Inherited mutations in the TP53 gene are associated with Li-Fraumeni syndrome (LFS), which is a rare cancer predisposition syndrome that increases risk for various cancers throughout a person’s lifetime. Sometimes it is unclear if a TP53 mutation found by genetic testing was acquired or inherited. Understanding the origin of the mutation will better inform the individual’s risk for cancer, as well as cancer risk for their relatives.</p>

